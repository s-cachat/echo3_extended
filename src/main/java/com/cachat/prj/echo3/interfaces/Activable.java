package com.cachat.prj.echo3.interfaces;

/**
 * Un objet avec une notion d'activation. Cet objet doit etre une entite
 */
public interface Activable{

    /**
     * Getter for property actif.
     * @return Value of property actif.
     */
    public boolean getActif() ;

    /**
     * Setter for property actif.
     * @param actif New value of property actif.
     */
    public void setActif(boolean actif) ;
}


